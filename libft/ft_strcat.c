#include "libft.h"

char	*ft_strcat(char *s1, char *s2)
{
	int x;
	int y;

	x = 0;
	y = 0;
	while (s1[y] != '\0')
	{
		y++;
	}
	while (s2[x] != '\0')
	{
		s1[y + x] = s2[x];
		x++;
	}
	s1[x + y] = '\0';
	return (s1);
}
