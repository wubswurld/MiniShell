#include "libft.h"

int		ft_nbr_len(int n)
{
	int neg;
	int x;

	neg = 0;
	x = 0;
	if (n < 0)
	{
		n *= -1;
		neg = 1;
	}
	while (n / 10)
	{
		x++;
		n /= 10;
	}
	return ((x + neg));
}
