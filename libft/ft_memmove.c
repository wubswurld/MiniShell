#include "libft.h"

void	*ft_memmove(void *s1, const void *s2, size_t n)
{
	size_t x;

	x = n;
	while (s1 > s2 && x-- > 0)
	{
		((char *)s1)[x] = ((char *)s2)[x];
	}
	x = 0;
	while (s1 < s2 && x < n)
	{
		((char *)s1)[x] = ((char *)s2)[x];
		x++;
	}
	return (s1);
}
