#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	x;
	size_t	a;

	x = 0;
	a = 0;
	while (s1[x])
		x++;
	while (s2[a] && a < n)
	{
		s1[x] = s2[a];
		x++;
		a++;
	}
	s1[x] = '\0';
	return (s1);
}
