#include "libft.h"

void	ft_striteri(char *s1, void (*f) (unsigned int, char *))
{
	int		x;

	x = 0;
	if (!s1 || !f)
		return ;
	while (*s1 != '\0')
	{
		f(x, s1);
		x++;
		s1++;
	}
}
